#Aritméticos
soma = 10 + 5
print("Soma: " + str(soma))
subtracao = 10 - 5
print(f"subtracao {subtracao}")
multiplicacao = 10 * 5
print(multiplicacao)
divisao = 10 / 5
print(divisao)
resto = 10 % 3
print(resto)
potencia = 2 ** 2
print(potencia)

#Comparação (retornam True ou False)
print(10 > 5)   #True
print(10 == 5)  #False
print(10 != 5)  #True

#Lógicos
print(True and False)  #False
print(True or False)   #True
print(not True)        #False